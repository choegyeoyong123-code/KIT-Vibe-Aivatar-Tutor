/**
 * SVG → WebP + LQIP(blur) 생성 후 TypeScript 상수 모듈로 출력합니다.
 * 실행: node scripts/generate-persona-image-assets.mjs
 */
import sharp from "sharp";
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const personaDir = path.join(root, "public", "images", "personas");

const IDS = [
  "warm_instructor",
  "strict_coach",
  "energetic_rapper",
  "zen_guide",
  "curious_explorer",
];

const OUT_W = 256;
const OUT_H = 256;
const BLUR_SIZE = 12;

async function main() {
  const entries = [];

  for (const id of IDS) {
    const svgPath = path.join(personaDir, `${id}.svg`);
    if (!fs.existsSync(svgPath)) {
      throw new Error(`Missing ${svgPath}`);
    }

    const webpPath = path.join(personaDir, `${id}.webp`);
    await sharp(svgPath)
      .resize(OUT_W, OUT_H, {
        fit: "contain",
        background: { r: 0, g: 0, b: 0, alpha: 0 },
      })
      .webp({ quality: 88, effort: 6 })
      .toFile(webpPath);

    const blurBuf = await sharp(svgPath)
      .resize(BLUR_SIZE, BLUR_SIZE, {
        fit: "contain",
        background: { r: 0, g: 0, b: 0, alpha: 0 },
      })
      .webp({ quality: 12 })
      .toBuffer();

    const blurDataURL = `data:image/webp;base64,${blurBuf.toString("base64")}`;
    entries.push({ id, blurDataURL });
    console.log(`OK ${id}.webp + LQIP (${blurBuf.length} bytes)`);
  }

  const ts = `/* eslint-disable max-len -- generated LQIP data URLs */
/**
 * Auto-generated by scripts/generate-persona-image-assets.mjs
 * Do not edit by hand — re-run the script after SVG changes.
 */
import type { DynamicPersonaId } from "@/lib/agent/persona/types";

export const PERSONA_IMAGE_INTRINSIC = { width: ${OUT_W}, height: ${OUT_H} } as const;

export const PERSONA_IMAGE_BLUR: Record<DynamicPersonaId, string> = {
${entries.map((e) => `  "${e.id}": ${JSON.stringify(e.blurDataURL)},`).join("\n")}
};
`;

  const outFile = path.join(
    root,
    "src",
    "lib",
    "persona-gallery",
    "persona-image-assets.generated.ts",
  );
  fs.mkdirSync(path.dirname(outFile), { recursive: true });
  fs.writeFileSync(outFile, ts, "utf8");
  console.log(`Wrote ${outFile}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
